// Aritiméticos
let x = 10;
let y = 5;

console.log(x + y);
console.log(x - y);
console.log(x / y);
console.log(x * y);
console.log(x % y);
x++;
y--;
console.log("---------\n");
// Comparação
console.log(10 == "10");
console.log(10 === "10"); // igual 3 vezes, verifica a semelhança do valor e o tipo do dado
console.log(100 > 10);
console.log(100 < 10);
console.log(10 != 5);
console.log(10 !== "10");

console.log("---------\n");
// Lógicos

let a = true;
let b = false;

console.log(a && b);
console.log(a || b);
console.log(!a);
